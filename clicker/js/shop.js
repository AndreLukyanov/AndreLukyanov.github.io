// Открытие ЛАВКИ //

function visible() {
	let shop = document.getElementById("armor");

	shop.classList.toggle("armor-visible");
}

// Создание блоков в цикле и вывод в лавку

function addInShopArmor() {
	for (let i = 0; i < shop.length; i++) {
		let shopItem = document.createElement("div");
		let shopImg = document.createElement("img");
		let shopPrice = document.createElement("span");
		let shopAudio = document.createElement("audio");
		let shopAudioSource = document.createElement("source");


		shopItem.className = "armor__item";
		document.querySelector(".armor__body").appendChild(shopItem);


		Array.prototype.forEach.call(document.querySelectorAll(".armor__item"), function (e) {
			e.appendChild(shopImg);
			e.appendChild(shopPrice);
			e.appendChild(shopAudio);
			shopImg.className = "armor__images";
			shopPrice.className = "armor__price";
			shopAudio.className = "armor__audio";
		});

		Array.prototype.forEach.call(document.querySelectorAll(".armor__audio"), function (a) {
			a.appendChild(shopAudioSource);
			shopAudioSource.className = "armor__source";
		});

		shopImg.src = shop[i].url; // Добавление пути к img

		shopPrice.innerHTML = shop[i].price; // Добавление цены

		shopAudioSource.type = shop[i].subjectSound[0].type;
		shopAudioSource.src = shop[i].subjectSound[0].src;
	}
};

addInShopArmor();